const mongoose = require('mongoose');
require('dotenv').config();

const connectDB = async () => {
  try {
    const conn = await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/geisha_garden', {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    });

    console.log(`‚úÖ MongoDB connect√©e: ${conn.connection.host}`);
    return conn;
  } catch (error) {
    console.error('‚ùå Erreur de connexion MongoDB:', error.message);
    process.exit(1);
  }
};

const disconnectDB = async () => {
  try {
    await mongoose.connection.close();
    console.log('üîå MongoDB d√©connect√©e');
  } catch (error) {
    console.error('‚ùå Erreur de d√©connexion MongoDB:', error.message);
  }
};

module.exports = {
  connectDB,
  disconnectDB,
  connection: mongoose.connection
};
